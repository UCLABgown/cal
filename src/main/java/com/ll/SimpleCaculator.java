package com.ll;

public class SimpleCaculator {
    public double plus(double n1,double n2){
        return n1+n2;
    }
    public double sub(double n1, double n2){
        return n1-n2;
    }
    public double mul(double n1, double n2){
        return n1*n2;
    }
    public double divide(double n1, double n2){
        return n1/n2;
    }
}
